// Math.ceil for  around to the  bigest intger number 
const num1 = Math.ceil(9.12); //10
console.log(num1); 
const num2 = Math.ceil(-9.12); //-9 
console.log(num2);

// Math.round for around  to near intger number 
const num3 = Math.round(9.12); //9
console.log(num3); 
const num4 = Math.round(9.52); //10
console.log(num4); 

// Math.floor for  around to the  smallest intger number 
const num5 = Math.floor(9.12); // 9
console.log(num5); 
const num6 = Math.floor(-9.12); // -10
console.log(num6); 